package com.stringMethods;

public class StringToLowerCase {
	
	
	public static void main(String[] args) {
		
		String s = "THIS IS LINE with SOME UPPER case and lOWER CASE"; //declaring string by literal
		 
		String lower = s.toLowerCase(); // All word will be converted to lower case
		
		System.out.println(lower);
		
	}

}
